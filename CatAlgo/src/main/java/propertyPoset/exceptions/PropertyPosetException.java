package propertyPoset.exceptions;

public class PropertyPosetException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5704569659471311616L;

	public PropertyPosetException() {
	}

	public PropertyPosetException(String arg0) {
		super(arg0);
	}

	public PropertyPosetException(Throwable arg0) {
		super(arg0);
	}

	public PropertyPosetException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PropertyPosetException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
